friends = ["Rolf", "Charlie", "Anna", "Bob", "Jen"]

print(friends[2:4])
print(friends[2:])
print(friends[:4])

print(friends[:])

print(friends[-3:])
print(friends[:-2])
print(friends[-3:-1])

